class Passage {
    _id;
    _day;
    _number;


    get id() {
        return this._id;
    }

    set id(value) {
        this._id = value;
    }

    get day() {
        return this._day;
    }

    set day(value) {
        this._day = value;
    }

    get number() {
        return this._number;
    }

    set number(value) {
        this._number = value;
    }
}
